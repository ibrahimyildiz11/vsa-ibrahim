
//invoer
let getal_1 = parseInt(prompt("Getal 1"));
let getal_2 = parseInt(prompt("Getal 2"));

// verwerking

let optelling = getal_1+getal_2;
let aftrekking = getal_1 - getal_2 ;
let vermenigvuldiging = getal_1 * getal_2 ;
let deling = getal_1 / getal_2 ;

// uitvoer
console.log("Optelling van de twee getallen: " + optelling);
console.log("Aftrekking van de twee getallen: " + aftrekking);
console.log("Vermenigvuldiging van de twee getallen: " + vermenigvuldiging);
console.log("Deling van de twee getallen: " + deling);